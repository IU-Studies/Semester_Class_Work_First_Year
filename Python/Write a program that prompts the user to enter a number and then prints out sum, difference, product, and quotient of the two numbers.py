# Write a program that prompts the user to enter a number and then prints out sum, difference, product, and quotient of the two numbers.

num1 = float(input("Enter number one:-"))
num2 = float(input("Enter number two:-"))

print ("Sum of entered number is",num1+num2)
print ("Difference of entered number is",num1-num2)
print ("Product of entered number is",num1*num2)
print ("Quotient of entered number is",num1/num2)
